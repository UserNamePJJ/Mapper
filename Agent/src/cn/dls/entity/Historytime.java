package cn.dls.entity;

public class Historytime {
	
	public int id;
	public int userid;
	public String time;
	public String sh;
	public String miao;
	
	
	public String getSh() {
		return sh;
	}
	public void setSh(String sh) {
		this.sh = sh;
	}
	public String getMiao() {
		return miao;
	}
	public void setMiao(String miao) {
		this.miao = miao;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	

}
